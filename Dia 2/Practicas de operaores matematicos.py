x=3
y=5
z=2
# Practica de suma
print(f"La suma de {x} e {y} es {x+y}")
# Practica de división al piso
print(f"La división al piso (redondeada) de {y} dividido {z} es {y//z}")
# Practica de modulo
print(f"El modulo de {y} dividido {z} es {y%z}")
# Practica de potencia
print(f"{z} elevado a la {5} da como resultado {z**5}")